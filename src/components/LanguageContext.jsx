const LanguageContext = createContext();

export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState(() => {
    if (typeof window !== 'undefined') {
      return localStorage.getItem('znpcv_language') || 'de';
    }
    return 'de';
  });

  const [darkMode, setDarkMode] = useState(() => {
    if (typeof window !== 'undefined') {
      return localStorage.getItem('znpcv_darkmode') !== 'false';
    }
    return true;
  });

  useEffect(() => {
    localStorage.setItem('znpcv_language', language);
  }, [language]);

  useEffect(() => {
    localStorage.setItem('znpcv_darkmode', darkMode.toString());
  }, [darkMode]);

  const t = (key) => translations[language][key] || key;
  
  const toggleLanguage = () => {
    const langs = ['de', 'en', 'fa', 'zh', 'es', 'fr', 'ar', 'hi', 'ja', 'pt'];
    const currentIndex = langs.indexOf(language);
    setLanguage(langs[(currentIndex + 1) % langs.length]);
  };

  const toggleDarkMode = () => setDarkMode(!darkMode);
  
  const isRTL = language === 'fa' || language === 'ar' || language === 'hi';

  return (
    <LanguageContext.Provider value={{ language, setLanguage, toggleLanguage, t, isRTL, darkMode, setDarkMode, toggleDarkMode }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}

export function LanguageToggle() {
  const { language, setLanguage, darkMode } = useLanguage();
  const [isOpen, setIsOpen] = React.useState(false);
  
  const languages = [
    { code: 'de', label: 'DE', flag: '🇩🇪' },
    { code: 'en', label: 'EN', flag: '🇬🇧' },
    { code: 'zh', label: 'ZH', flag: '🇨🇳' },
    { code: 'es', label: 'ES', flag: '🇪🇸' },
    { code: 'fr', label: 'FR', flag: '🇫🇷' },
    { code: 'pt', label: 'PT', flag: '🇵🇹' },
    { code: 'hi', label: 'HI', flag: '🇮🇳' },
    { code: 'ja', label: 'JA', flag: '🇯🇵' },
    { code: 'ar', label: 'AR', flag: '🇸🇦' },
    { code: 'fa', label: 'FA', flag: '🇮🇷' }
  ];
  
  const currentLang = languages.find(l => l.code === language) || languages[0];
  
  return (
    <div className="relative">
      <button
        onClick={() => setIsOpen(!isOpen)}
        className="flex items-center justify-center gap-1 px-2 py-2 transition-all"
      >
        <span className={`text-[10px] font-bold tracking-widest ${darkMode ? 'text-white' : 'text-black'}`}>{currentLang.label}</span>
        <span className="text-sm">{currentLang.flag}</span>
      </button>
      
      {isOpen && (
        <>
          <div className="fixed inset-0 z-10" onClick={() => setIsOpen(false)} />
          <div className={`absolute right-0 mt-2 w-44 rounded-xl border-2 z-20 overflow-hidden ${
            darkMode ? 'bg-zinc-900 border-zinc-800' : 'bg-white border-zinc-300'
          }`}>
            <div className="grid grid-cols-2 gap-1 p-2">
              {languages.map((lang) => (
                <button
                  key={lang.code}
                  onClick={() => {
                    setLanguage(lang.code);
                    setIsOpen(false);
                  }}
                  className={`px-2 py-2 rounded-lg transition-all flex items-center justify-center gap-1 text-[10px] font-bold tracking-widest ${
                    language === lang.code 
                      ? darkMode 
                        ? 'bg-white text-black' 
                        : 'bg-zinc-900 text-white'
                      : darkMode 
                        ? 'text-zinc-400 hover:text-white hover:bg-zinc-800' 
                        : 'text-zinc-600 hover:text-black hover:bg-zinc-100'
                  }`}
                >
                  {lang.label}
                  <span className="text-sm">{lang.flag}</span>
                </button>
              ))}
            </div>
          </div>
        </>
      )}
    </div>
  );
}

